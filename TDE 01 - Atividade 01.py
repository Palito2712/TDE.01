numeroInteiro = int(input("Para saber o sucessor e antecessor de um número inteiro, por favor, digite um número:  "))

numeroSucessor = numeroInteiro + 1

numeroAntecessor = numeroInteiro - 1

print("O número", numeroInteiro, "tem como sucessor o número", numeroSucessor, "e o seu antecessor é o número", numeroAntecessor)